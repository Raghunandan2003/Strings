package StringProgram;

public class Str15 {

	
	public static void main(String[] args) {
		int n[]= {1,2,3,4};
		
		for(int i=0;i<n.length;i++)
		{
			for(int j=0;j<n.length;j++)
			{
				for(int k=0;k<=i+1;k++)
				{
					System.out.print(k);
				}
				System.out.println();
			}
		}
	}
}
		
		
		
		
															
	

